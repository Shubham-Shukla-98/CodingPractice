class Solution {
public:
    
    int res = 0;
    
    int findPath(vector<vector<int>>& gr, int i, int j, vector<vector<int>>& dp)
    {
        if(i == gr.size()-1 && j == gr[0].size() - 1)
        {
            if(gr[i][j] == 1)
                return 0;
            return 1;
        }
        else
        {
            int res = 0;
            if(i+1 < gr.size())
            {
                if(gr[i+1][j] == 0)
                {
                    if(dp[i+1][j] > -1)
                        res += dp[i+1][j];
                    else
                    {
                        dp[i+1][j] = findPath(gr, i+1, j, dp);
                        res += dp[i+1][j];
                    }
                }
            }
            if(j+1 < gr[0].size())
            {
                if(gr[i][j+1] == 0)
                {
                    if(dp[i][j+1] > -1)
                        res += dp[i][j+1];
                    else
                    {
                        dp[i][j+1] = findPath(gr, i, j+1, dp);
                        res += dp[i][j+1];
                    }
                }
            }
            dp[i][j] = res;
            return res;
        }
    }
    
    int uniquePathsWithObstacles(vector<vector<int>>& obstacleGrid) {
        
        vector<vector<int>> dp(obstacleGrid.size() + 1 , vector<int>(obstacleGrid[0].size()+1, -1));
        return findPath( obstacleGrid, 0, 0, dp);
    }
};

